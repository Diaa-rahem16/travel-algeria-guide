class Place {
  final String placeID;
  final String? name;
  final String? state;
  final String? location;
  final String? category;
  final String? imagePath;
  final String? description;
  final int? likes;

  Place({required this.placeID, this.name, this.state, this.location, this.category, this.imagePath, this.description, this.likes}); 
}